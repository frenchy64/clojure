#!/bin/bash
# Quick focused synthetic benchmark for nil optimization
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
RESULTS_DIR="$SCRIPT_DIR/quick-results"
mkdir -p "$RESULTS_DIR"

echo "=========================================="
echo "Quick Nil Optimization Analysis"
echo "=========================================="
echo ""

# Download baseline Clojure 1.12.3
if [ ! -f "$RESULTS_DIR/clojure-baseline.jar" ]; then
    echo "Downloading Clojure 1.12.3..."
    curl -sL -o "$RESULTS_DIR/clojure-baseline.jar" \
        https://repo1.maven.org/maven2/org/clojure/clojure/1.12.3/clojure-1.12.3.jar
fi

# Build optimized version
echo "Building optimized Clojure..."
cd "$SCRIPT_DIR/../.."
mvn -q -B clean package -Dmaven.test.skip=true -Plocal 2>&1 | tail -5
BUILT_JAR=$(find target -name "clojure-*.jar" -not -name "*-slim.jar" -not -name "*-sources.jar" | head -1)
cp "$BUILT_JAR" "$SCRIPT_DIR/quick-results/clojure-optimized.jar"

# Create simple test code
mkdir -p "$RESULTS_DIR/test-src"
cat > "$RESULTS_DIR/test-src/test_nil.clj" << 'CLOJURE'
(ns test-nil)

;; Simple macro with nil
(defmacro when-not-test [x]
  `(if ~x nil :result))

;; Function using the macro
(defn use-when-not []
  (when-not-test false))

;; Macro with multiple nils
(defmacro return-nils []
  `[nil nil nil])

;; Function using multiple nils
(defn use-nils []
  (return-nils))
CLOJURE

# Compile with baseline
echo ""
echo "Compiling with baseline Clojure 1.12.3..."
mkdir -p "$RESULTS_DIR/baseline-classes"
cd "$RESULTS_DIR/baseline-classes"
java -cp "$RESULTS_DIR/clojure-baseline.jar:$RESULTS_DIR/test-src" \
    -Dclojure.compile.path="$RESULTS_DIR/baseline-classes" \
    -Dclojure.compiler.direct-linking=true \
    clojure.main -e "(compile 'test-nil)" 2>&1 | head -5

# Compile with optimized
echo "Compiling with optimized Clojure..."
mkdir -p "$RESULTS_DIR/optimized-classes"
cd "$RESULTS_DIR/optimized-classes"
java -cp "$RESULTS_DIR/clojure-optimized.jar:$RESULTS_DIR/test-src" \
    -Dclojure.compile.path="$RESULTS_DIR/optimized-classes" \
    -Dclojure.compiler.direct-linking=true \
    clojure.main -e "(compile 'test-nil)" 2>&1 | head -5

# Compare class files
echo ""
echo "Comparing generated bytecode..."
cd "$SCRIPT_DIR"

for baseline_class in $(find "$RESULTS_DIR/baseline-classes" -name "*.class" -type f); do
    rel_path="${baseline_class#$RESULTS_DIR/baseline-classes/}"
    optimized_class="$RESULTS_DIR/optimized-classes/$rel_path"
    
    if [ -f "$optimized_class" ]; then
        if ! cmp -s "$baseline_class" "$optimized_class"; then
            echo ""
            echo "CHANGED: $rel_path"
            echo "----------------------------------------"
            echo "Baseline bytecode:"
            javap -c "$baseline_class" | grep -A 30 "when_not_test\|return_nils" | head -35
            echo ""
            echo "Optimized bytecode:"
            javap -c "$optimized_class" | grep -A 30 "when_not_test\|return_nils" | head -35
        fi
    fi
done

echo ""
echo "=========================================="
echo "Analysis complete. Results in: $RESULTS_DIR"
